var type_c = 'bar';


getData();

function getData() {
    $.ajax({
        type: "get",
        url: "api/b/get-thongke",
        success: function (response) {
            var data_1 = [];

            $.each(response, function (i, item) {
                data_1[item.thang] = parseInt(item.chi_phi);
            });
            for (let i = 0; i <= 12; i++) {
                if (data_1[i] == undefined) {
                    data_1[i] = 0;
                } else {
                    data_1[i] = data_1[i] / 1000000;
                }
            }
            draw_chart(data_1, type_c);
        }
    });
}
